$(document).ready(function () {
  var doc = $(document);
  if (doc.find("canvas").is('#mainHeaderLogoCanvas')) {
    mainHeaderLogo.init();
  }
  if (doc.find("div").is('#mainSlider')) {
    mainSlider.init();
  }
});
